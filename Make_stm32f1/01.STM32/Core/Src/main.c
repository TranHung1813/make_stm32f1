static void task_wdt(void)
{
    static uint32_t m_tick = 0;
    if (m_sys_tick - m_tick >= (uint32_t)300)
    {
        m_tick = m_sys_tick;
        board_hw_watchdog_feed();
    }
}

uint32_t HAL_GetTick(void)
{
    return m_sys_tick;
}

